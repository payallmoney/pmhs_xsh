package cn.net.tongfang.framework.security.bean;

public class VacciProgram {
	private Integer rowNumber;
	private String age;
	private String hepB;
	private String bcg;
	private String opv;
	private String dpt;
	private String dt;
	private String mv;
	private String mr;
	private String mmr;
	private String mm;
	private String je;
	private String aecm;
	private String acecm;
	private String aha;
	private String iha;
	
	public VacciProgram() {
		super();
	}

	public VacciProgram(Integer rowNumber, String age, String hepB, String bcg,
			String opv, String dpt, String dt, String mv, String mr,
			String mmr, String mm, String je, String aecm, String acecm,
			String aha, String iha) {
		super();
		this.rowNumber = rowNumber;
		this.age = age;
		this.hepB = hepB;
		this.bcg = bcg;
		this.opv = opv;
		this.dpt = dpt;
		this.dt = dt;
		this.mv = mv;
		this.mr = mr;
		this.mmr = mmr;
		this.mm = mm;
		this.je = je;
		this.aecm = aecm;
		this.acecm = acecm;
		this.aha = aha;
		this.iha = iha;
	}

	public Integer getRowNumber() {
		return rowNumber;
	}

	public void setRowNumber(Integer rowNumber) {
		this.rowNumber = rowNumber;
	}

	public String getAge() {
		return age;
	}

	public void setAge(String age) {
		this.age = age;
	}

	public String getHepB() {
		return hepB;
	}

	public void setHepB(String hepB) {
		this.hepB = hepB;
	}

	public String getBcg() {
		return bcg;
	}

	public void setBcg(String bcg) {
		this.bcg = bcg;
	}

	public String getOpv() {
		return opv;
	}

	public void setOpv(String opv) {
		this.opv = opv;
	}

	public String getDpt() {
		return dpt;
	}

	public void setDpt(String dpt) {
		this.dpt = dpt;
	}

	public String getDt() {
		return dt;
	}

	public void setDt(String dt) {
		this.dt = dt;
	}

	public String getMv() {
		return mv;
	}

	public void setMv(String mv) {
		this.mv = mv;
	}

	public String getMr() {
		return mr;
	}

	public void setMr(String mr) {
		this.mr = mr;
	}

	public String getMm() {
		return mm;
	}

	public void setMm(String mm) {
		this.mm = mm;
	}

	public String getMmr() {
		return mmr;
	}

	public void setMmr(String mmr) {
		this.mmr = mmr;
	}

	public String getJe() {
		return je;
	}

	public void setJe(String je) {
		this.je = je;
	}

	public String getAecm() {
		return aecm;
	}

	public void setAecm(String aecm) {
		this.aecm = aecm;
	}

	public String getAcecm() {
		return acecm;
	}

	public void setAcecm(String acecm) {
		this.acecm = acecm;
	}

	public String getAha() {
		return aha;
	}

	public void setAha(String aha) {
		this.aha = aha;
	}

	public String getIha() {
		return iha;
	}

	public void setIha(String iha) {
		this.iha = iha;
	}
	
}
